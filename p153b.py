def multiplica(uno, dos):
    """ Función que multiplica dos números y retorna el resultado"""
    return uno * dos

def suma(uno, dos):
    """ Función que multiplica dos números y retorna el resultado"""
    return uno + dos

def cuadrado(num):
    """ Función que retorna el cuadrado de un número"""
    return num * num
